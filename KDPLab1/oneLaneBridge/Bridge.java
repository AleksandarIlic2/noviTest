package oneLaneBridge;

public class Bridge {

	public int northCnt;
	public int southCnt;
	
	public int crossN = 0;
	public int crossS = 0;
	
	public int waitingN = 0;
	public int waitingS = 0;
	
	public Bridge() {
		this.northCnt = 0;
		this.southCnt = 0;
		
		this.crossN = 0;
		this.crossS = 0;
		
		this.waitingN = 0;
		this.waitingS = 0;		
		
	}
	
	
}
